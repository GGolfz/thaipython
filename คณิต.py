import math
class คณิต:
    @staticmethod
    def เพดาน(x):
        return math.ceil(x)
    @staticmethod
    def พื้น(x):
        return math.floor(x)
    @staticmethod
    def แฟกทอเรียล(x):
        return math.factorial(x)
    @staticmethod
    def ค่าสัมบูรณ์(x):
        return math.fabs(x)
    @staticmethod
    def ยกกำลัง(x,y):
        return math.pow(x,y)
    @staticmethod
    def รากที่สอง(x):
        return math.sqrt(x)